package com.ibm.domain;

public class MeetingTypeCodeDomain {
	String meetingTypeID;
	String meetingTypeNM;
	String meetingTypeDesc;
	String codeColor;
	
	public String getMeetingTypeID() {
		return meetingTypeID;
	}
	public void setMeetingTypeID(String meetingTypeID) {
		this.meetingTypeID = meetingTypeID;
	}
	public String getMeetingTypeNM() {
		return meetingTypeNM;
	}
	public void setMeetingTypeNM(String meetingTypeNM) {
		this.meetingTypeNM = meetingTypeNM;
	}
	public String getMeetingTypeDesc() {
		return meetingTypeDesc;
	}
	public void setMeetingTypeDesc(String meetingTypeDesc) {
		this.meetingTypeDesc = meetingTypeDesc;
	}
	public String getCodeColor() {
		return codeColor;
	}
	public void setCodeColor(String codeColor) {
		this.codeColor = codeColor;
	}
	
}
