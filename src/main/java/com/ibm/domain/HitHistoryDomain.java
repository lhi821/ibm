package com.ibm.domain;

public class HitHistoryDomain {
	 
	String memberId;
	String meetingNoteId;
	String hitDttm;
	String hitHistoryDiv;
	
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMeetingNoteId() {
		return meetingNoteId;
	}
	public void setMeetingNoteId(String meetingNoteId) {
		this.meetingNoteId = meetingNoteId;
	}
	public String getHitDttm() {
		return hitDttm;
	}
	public void setHitDttm(String hitDttm) {
		this.hitDttm = hitDttm;
	}
	public String getHitHistoryDiv() {
		return hitHistoryDiv;
	}
	public void setHitHistoryDiv(String hitHistoryDiv) {
		this.hitHistoryDiv = hitHistoryDiv;
	}
	
	
}
